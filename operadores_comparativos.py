# OPERADORES DE COMPARAÇÃO ( Utilizado para comparar 2 ou mais variáveis)

saldo = 500
saque = 450

# Igualdade

print(saldo==saque)

#Diferença

print(saldo!=saque)

# Maior que / Maior ou igual

print(saldo > saque)

print(saldo >= saque)

# Menor quw / Menor ou igual

print(saldo < saque)

print(saldo <= saque)